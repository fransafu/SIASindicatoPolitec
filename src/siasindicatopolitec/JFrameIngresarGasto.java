    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //Ingresar
        if (jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Ingrese descripcion de gasto");
        }
        else if (jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Ingrese Valor del gasto");
        }
        else{
        try {
            Statement instruccion = Database.conexion();

            String detalle = jTextField1.getText();
            String valor = jTextField2.getText();
             int ano = jDateChooser1.getCalendar().get(Calendar.YEAR);
            int mes= jDateChooser1.getCalendar().get(Calendar.MONTH);
            int dia = jDateChooser1.getCalendar().get(Calendar.DAY_OF_MONTH);
            String fecha = ano+"-"+mes+"-"+dia;
            int cod_egr=0;
            
            // Obtener ultimo cod_egreso en la base de dato
            ResultSet respuesta = instruccion.executeQuery("SELECT MAX(`cod_egreso`) FROM `egreso`");
            while (respuesta.next()) {
                if (respuesta.getString(1) != null){
                    cod_egr = Integer.parseInt(respuesta.getString(1));
                }
                else{
                    cod_egr = 0;
                }
            }
            cod_egr +=1; // aumenta cod_multa en 1 para agregar el siguiente
          
            String sql;
            sql = "INSERT INTO `egreso`(`descripcion`, `monto`) VALUES ('"
                + detalle + "',"
                + valor +");"; 
            
            sql = "INSERT INTO `presupuesto_mensual_egreso` (`fecha`,`presupuesto_mensual_id`,`egreso_id`) VALUES ('"
                + fecha + "',"
                + "(SELECT `presupuesto_mensual_id` FROM `registro_presupuesto` WHERE `fecha` = '" +fecha +
                "'),"
                + cod_egr +")";
            instruccion.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Gasto Ingresado Correctamente");

        }catch (SQLException ex) {
            Logger.getLogger(JFrameSocio.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

