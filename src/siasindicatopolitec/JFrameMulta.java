    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Registrar
        try {
            Statement instruccion = Database.conexion();

            String rut = jTextField1.getText();
            String numero_multa = jTextField2.getText();
            String motivo = (String) jComboBox1.getSelectedItem();
            String fecha = FechaActual();             

            String sql;
            sql = "INSERT INTO multa (`rut_id`, `motivo_id`,`fecha`, `cant_multa`) VALUES ("
                + rut +
               ", (SELECT `cod_motivo` FROM `motivo` WHERE tipo ='"
                + motivo +
                "'), '"
                + fecha +
                "','"
                + numero_multa +"');";
                
            instruccion.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Multa Registrada ");
            

        } catch (SQLException ex) {
            Logger.getLogger(JFrameSocio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private void comboBoxMotivo() throws SQLException{
        Statement instruccion = Database.conexion();
        String sql = "SELECT tipo FROM motivo";
        ResultSet lista = instruccion.executeQuery(sql);
        String varAux = "";

        List<String> aux = new ArrayList<String>();

        while(lista.next()){
            varAux = lista.getString(1);
            aux.add(varAux);
        }

        String[] data = new String[ aux.size() ];
        aux.toArray( data );

        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(data));
    }
    
    public static String FechaActual(){
        Date fecha = new Date();
        SimpleDateFormat formato = new SimpleDateFormat("YYYY-MM-dd");
        
        return formato.format(fecha);
        
    }
    
